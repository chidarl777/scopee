<p><h2><b>1. REFERRAL</b></h2>
(refer and earn)<br>
<b>user A</b> refers <b>User B</b><br />
for every earning makes, <b>user A</b> is rewarded 30% of what <b>user B</b> makes, for being <b>user B</b> referral<br />
They more you refer the more you earn.
</p>
<p>
	<h2><b>2. CONTENT POSTING </b></h2>
When <b>user A</b> create a content ,a tag containing an advert will be place along side with <b>user A</b> post, if <b>user A</b> and  B are friends<br/>
When <b>user B</b> clicks on the readmore button in <b>user A</b>'s post, on viewing <b>user A</b>'s Complete post, <b>user B</b> sees an adword along side <b>user A</b> post, if the advert shown to <b>user B</b> is of <b>user B</b>'s interest and <b>user B</b> clicks on that advert  <b>user A</b> earns 250 peecoins  for posting  and for allow us place an advert in his post.
  </p>
  <p> <h2><b>3. SHARING OTHERS POST OR YOUR POST</b></h2>
In Scopee you must not be the content creator to earn, you can be a post  sharer.<br/>
for example,  i will still make reference using <b>user A</b> and <b>user B</b><br/>
if <b>user A</b> has only one friend that is <b>user B</b><br/> 
and <b>user B</b> has 2 more friends which is <b>user C</b> and user D<br/>
when <b>user B</b>sees a post posted by <b>user A</b> in his timeline,<br/>
<b>user B</b> then shares the post to <b>user C</b> and user D <br/>
<b>user C</b> and D on viewing <b>user A</b> post clicks on an advert the found interesting to them<br/>
<b>user B</b> earns 50 peecoin for  being the post sharer.

</p>
<p>
	<h2><b>4. WORLD CREATION</b></h2>
	A user in scopee can also earn through world creation.<br/>
	A world is a page customized to fit they user's bussiness or brand needs.<br/>
	if <b>user A</b> create's a world, for every valid click made from <b>user A</b>'s world post, weather posted by <b>user A</b> or by those <b>user A</b> give permission to post in his world, they world creator(<b>user A</b>) earns 100 peecoin.
	
</p>
<p>
	<h2><b>5. WORLD POST</b></h2>
	In scopee a user can also earn by being a content creator in a world, weather created by him or he was given permission to post by other world creator's.<br/>
	If a valid click is made on an advert in his created post, the content creator earns 150 peecoin<br/>
	using the example in 4<br/>
	if <b>user B</b> post in <b>user A</b> world, and clicks on an advert is made from <b>user B</b> post.
<b>user B also earns 150 peecoin for being the content creator.</b>
